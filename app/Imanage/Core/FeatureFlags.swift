import Foundation

// MARK: - FeatureFlags
// 未完成・不安定な機能をビルド単位で切り離すためのフラグ。
// 実装は残したまま、UI と常駐処理の入口だけを塞ぐ。

enum FeatureFlags {
    /// フォルダ監視（フォルダ A に届いた写真を自動で B へ振り分ける常駐機能）。
    ///
    /// 無効化の理由:
    /// - 監視に適用される階層を指定できず、メイン画面で選択中の階層をそのまま参照してしまう。
    ///   ウィンドウを閉じても常駐するため、メイン画面で一時的に階層を変えたことを忘れると、
    ///   意図しない構成で写真が移動される。
    /// - FSEvents の発火から 2 秒待って処理を始める実装のため、大量コピーや低速な
    ///   外部ストレージでは書き込み途中のファイルを掴む可能性がある。
    ///
    /// 監視専用の階層設定を入れ、取り込み完了の判定を堅くしてから true に戻すこと。
    static let folderWatcher = false
}
